namespace Umbraco9KeyValueList.Models
{
    /// <summary>
    /// Model class to represent a key value pair.
    /// </summary>
    internal class KeyValueEditorPair
    {
        /// <summary>
        /// The key of the current pair.
        /// </summary>
        public string Key { get; set; }

        /// <summary>
        /// The value of the current pair.
        /// </summary>
        public string Value { get; set; }
    }
}